         public ArrayList<rappel> Rappel_list_today() {
        ArrayList<rappel> rappel_lists = new ArrayList<rappel>();
        String select_nom_type_act_today = "select * from rappel where (login=?) and date=CURDATE() order by heure";
        PreparedStatement stmt;
        ResultSet rs;
        int count = 0;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/test", "root", "");
            stmt = con.prepareStatement(select_nom_type_act_today);
            stmt.setString(1, Utilisateur.getLogin());
            rs = stmt.executeQuery();
            
            while (rs.next()) {
                emptytable2 = false;
                rappel act = new rappel(rs.getString("rappel"), rs.getString("login"),  rs.getString("nom"), rs.getString("heure"), rs.getDate("date").toString());
                rappel_lists.add(act);
                count++;
            }
            if(count<1) emptytable2=true;
            
        } catch (ClassNotFoundException e1) {
            System.out.println(e1);

        } catch (SQLException e1) {

            System.out.println(e1);
        }

        return rappel_lists;
    }
    
public void populate_jTable_today_from_db() {
        ArrayList<rappel> dataarray = Rappel_list_today();
        model5 = (DefaultTableModel) jTable1.getModel();
        Object[] rows = new Object[3];
        for (int i = 0; i < dataarray.size(); i++) {
            rows[0] = dataarray.get(i).get_rappel();
            rows[1] = dataarray.get(i).get_nom();
            rows[2] = dataarray.get(i).get_heure();
            model5.addRow(rows);
        }
    }


